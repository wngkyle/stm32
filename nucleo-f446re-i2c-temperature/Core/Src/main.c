/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
  HAL_StatusTypeDef ret; // For storing the return values from communication
  uint8_t buf[16]; // Buffer to temporarily storing string to send to computer
  int16_t val; // Signed 16 bit integer for storing raw temperature value
  float temp_c; // For storing processed temperature value, will be transmitted to PC through UART
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	 // Tells TMP102 that I want to read data from the temperature register
	 // Configure the pointer register first to prepare for the read operation later
	 buf[0] = REG_TEMP;
	 // Transmit 1 byte
	 ret = HAL_I2C_Master_Transmit(&hi2c1, TMP102_ADDR, buf, 1, HAL_MAX_DELAY); // HAL_MAX_DELAY set the function as a timeout function, so the code won't continue until this I2C communication is conducted
	 if (ret != HAL_OK) {
		 strcpy((char*)buf, "Error Tx\r\n");
	 } else {
		 // Reads 2 bytes from the temperature register
		 ret = HAL_I2C_Master_Receive(&hi2c1, TMP102_ADDR, buf, 2, HAL_MAX_DELAY); // HAL_MAX_DELAY set the function as a timeout function, so the code won't continue until this I2C communication is conducted
		 if (ret != HAL_OK) {
			 strcpy((char*)buf, "Error Tx\r\n");
		 } else {
			 // Combine the bytes
			 // Byte 1 is the most significant bit
			 // Byte 2 is the least significant bit
			 val = ((int16_t)buf[0] << 4) | (buf[1] >> 4);

			 // Check if the value is negative, check for 2's complement, see if value is greater than 0b0000 0111 1111 1111
			 // Convert to 2's complement, since temperature can be negative
			 if (val > 0x7FF) {
				 val |= 0xF000; // If value is negative, do sign extension
			 }

			 // Convert to float temperature value (Celsius)
			 temp_c = val * 0.0625;

			 // Convert temperature to decimal format
			 // The 02 in %02u add padding to the integer ensuring it's at least two digits long, 7 -> 07
			 temp_c *= 100;
			 sprintf((char*)buf,
					 "%u.%02u C\r\n",
					 ((unsigned int)temp_c / 100),
					 ((unsigned int)temp_c % 100));
		 }
	 }

	 // Send out the message in the buffer to PC, could be temperature value or error message
	 HAL_UART_Transmit(&huart2, buf, strlen((char*)buf), HAL_MAX_DELAY);
	 HAL_Delay(500);

	 // Sample data rate at twice per second
	 HAL_Delay(500);


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

